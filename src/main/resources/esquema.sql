CREATE TABLE tprodutopreco (
  ID INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1, INCREMENT BY 1) PRIMARY KEY,
  PRODUTO varchar(255) NOT NULL,
  TARIFA varchar(255) NOT NULL,
  CANAL varchar(255) NOT NULL,
  FORMA_PAGAMENTO varchar(30) NOT NULL,
  QTD_CARTAO_MIN int(11) NOT NULL,
  QTD_CARTAO_MAX int(11) NOT NULL,
  QTD_PAPEL_MIN int(11) DEFAULT NULL,
  QTD_PAPEL_MAX int(11) DEFAULT NULL,
  BENEFICIO_MAX double NOT NULL,
  REGIAO varchar(255) DEFAULT NULL,
  VLR_TARIFA_MIN double NOT NULL,
  QTD_DIAS_PRAZO_MAX int(11) NOT NULL,
  NIVEL_ALCADA int(11) NOT NULL,
  VOLUME_SVA_MAX int(11) DEFAULT NULL,
  VIGENCIA_MIN int(11) DEFAULT NULL,
  VERSAO varchar(20) DEFAULT NULL,
  DATA_INICIO_VIGENCIA datetime DEFAULT NULL,
  TIPO_CONTRATO varchar(100) DEFAULT NULL,
  PRIMARY KEY (ID)
);


CREATE TABLE ttaxasadicionais (
  ID INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1, INCREMENT BY 1) PRIMARY KEY,
  PRODUTO varchar(255) NOT NULL,
  TAXA_JUROS_DIA double NOT NULL,
  TAXA_SEGURO double NOT NULL,
  TAXA_IMPOSTO double NOT NULL,
  PRIMARY KEY (ID)
);